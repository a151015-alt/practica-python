def main():
    print("Calculadora sencilla en Python")
    
    num1 = float(input("Ingresa el primer número: "))
    num2 = float(input("Ingresa el segundo número: "))
    
    suma = num1 + num2
    resta = num1 - num2
    multiplicacion = num1 * num2
    
    print("Resultados:")
    print("Suma:", suma)
    print("Resta:", resta)
    print("Multiplicación:", multiplicacion)

if __name__ == "__main__":
    main()
